function getDistanceKm(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371
  const dLat = ((lat2 - lat1) * Math.PI) / 180
  const dLon = ((lon2 - lon1) * Math.PI) / 180
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLon / 2) ** 2
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
}

export function getClosestCity(lat: number, lon: number, maxRadiusKm?: number): City | undefined {
  let closestCoord: CoordCity | undefined
  let minDistance = Infinity

  for (const coord of coords.cities) {
    const distance = getDistanceKm(lat, lon, coord.lat, coord.lng)
    const withinRadius = maxRadiusKm === undefined || distance <= maxRadiusKm
    if (withinRadius && distance < minDistance) {
      minDistance = distance
      closestCoord = coord
    }
  }

  if (!closestCoord) return undefined

  for (const region of villes.regions) {
    for (const city of region.cities) {
      if (city.slug === closestCoord.url) return city
    }
  }

  return undefined
}

export function getCitiesInRadius(lat: number, lon: number, radiusKm: number): City[] {
  const result: City[] = []

  for (const coord of coords.cities) {
    if (getDistanceKm(lat, lon, coord.lat, coord.lng) <= radiusKm) {
      for (const region of villes.regions) {
        for (const city of region.cities) {
          if (city.slug === coord.url) result.push(city)
        }
      }
    }
  }

  return result
}

export function getCityByCityId(cityId: number): City | undefined {
  for (const region of villes.regions) {
    for (const city of region.cities) {
      if (city.cityIds.includes(cityId)) return city
    }
  }
  return undefined
}

export function getCoordByUrl(url: string): CoordCity | undefined {
  return coords.cities.find((c) => c.url === url)
}

